  val T: Int = 24

  /** Capacity (security parameter) */
  val C: Int = 13

  /** Rate (absorbable elements per permutation) */
  val R: Int = 11

  /** S-box exponent */
  val ALPHA: Int = 7

  /** Full rounds (split: R_F/2 before partial, R_F/2 after) */
  val R_F: Int = 8

  /** Partial rounds */
  val R_P: Int = 22

